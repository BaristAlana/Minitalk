#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	len;
	size_t	i;
	char	*tmp;

	tmp = (char *) s;
	len = ft_strlen(s);
	i = 0;
	while (i <= len)
	{
		if (*tmp == (char) c)
			return (tmp);
		++i;
		++tmp;
	}
	return (NULL);
}
